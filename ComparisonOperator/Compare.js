// Comparison Operator
let a = 5;
let b = 8;    // "8" is eligble           ( js convert string to number and then compare )

console.log("5 == 8   :", a == b);     // 5 == 8
console.log("5 != 8   :", a != b);     // is 5 not equal to 8 

console.log("a =", a, " , b =", b);
// === it compare Equal to & type
console.log("a === b  :", a === b);

// !== it compare not Equal to & type
console.log("a !== b  :", a !== b);

console.log("a > b  :", a > b);

console.log("a < b  :", a < b);

console.log("a <= b  :", a <= b);

console.log("a >= b  :", a >= b);






