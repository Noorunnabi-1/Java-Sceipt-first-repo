//Arithematic Operator 
let a = 7;
let b = 10;

console.log("a = ", a, "& b =", b);

console.log(" a + b = ", a + b);

console.log(" a - b = ", a - b);

console.log(" a * b = ", a * b);

console.log(" a / b = ", a / b);

// Modulus operator
a = 15;
b = 4;
console.log("a = ", a, "& b =", b);

console.log(" a % b = ", a % b);

// Exponential  a**b : means a to the power b 
a = 2;
b = 4;
console.log("a = ", a, "& b =", b);
console.log("a ** b = ", a ** b);

// Unary Operator
//Increment : a++ = a+1 same    (post increment)  (pre : ++a)
a = 2;
a++;   //a=a+1;

console.log("a++ = ", a);

//Decrement : a-- = a-1 same   (post increment)    (pre : --a)
a = 2;
a--;   //a=a-1;

console.log("a-- = ", a);

